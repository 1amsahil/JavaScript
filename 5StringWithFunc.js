let company = "  Samsung Co-operation  ";

// toUpperCase
console.log(company.toUpperCase());

// toLowerCase
console.log(company.toLowerCase());

// trim :- remove white space from start and end
console.log(company.trim());

let str1 = "Sony";
let str2 = "Co-operation";

// slice :- give the character from start to end (not countable)
console.log(str1.slice(1,4));

// concat
console.log(str1.concat(str2));

let greet = "hellollollo";

// replace
console.log(greet.replace("llo","y"));

// replaceAll
console.log(greet.replaceAll("llo","y"));

// charAt
console.log(company.charAt(3));